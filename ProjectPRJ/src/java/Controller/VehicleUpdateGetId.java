    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String vehicleIdParam = request.getParameter("vehicle_id");

        // Chuyển đổi ID phương tiện từ chuỗi thành int
        int vehicleId = Integer.parseInt(vehicleIdParam);
        VehicleDao dao = new VehicleDao();
        // Giả sử bạn có một phương thức để lấy thông tin phương tiện từ cơ sở dữ liệu
        Vehicle vehicle = dao.getVehicleById(vehicleId);

        request.setAttribute("vehicle", vehicle);

        request.getRequestDispatcher("VehicleUpdate.jsp").forward(request, response);

    }

